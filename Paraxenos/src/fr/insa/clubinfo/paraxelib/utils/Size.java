package fr.insa.clubinfo.paraxelib.utils;

public class Size {
	private final float width;
	private final float height;

	public Size(float width, float height) {
		this.width = width;
		this.height = height;
	}

	public float getWidth() {
		return width;
	}

	public float getHeight() {
		return height;
	}

}
